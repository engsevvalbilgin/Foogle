public void preOrder(BilginYilmazBNode node) {
        int sum = 0;
        if (node != null && node.file_list != null) {
            BilginYilmazFNode fnode = node.file_list.head;
            while (fnode != null) {
                sum += fnode.data;
                fnode = fnode.next;
            }
        }
        if (node != null) {
            txt_area_pre_in_post.append(node.data + "," + sum + "\n");
            node.printFileList();
            preOrder(node.left);
            preOrder(node.right);
        }

    }

    public void inOrder(BilginYilmazBNode node) {
        
        int sum = 0;
        if (node != null && node.file_list != null) {
            BilginYilmazFNode fnode = node.file_list.head;
            while (fnode != null) {
                sum += fnode.data;
                fnode = fnode.next;
            }
        }
        if (node != null) {
            inOrder(node.left);
            txt_area_pre_in_post.append(node.data + "," + sum + "\n");
            node.printFileList();
            inOrder(node.right);
        }
    }

    public void postOrder(BilginYilmazBNode node) {
        int sum = 0;
        if (node != null && node.file_list != null) {
            BilginYilmazFNode fnode = node.file_list.head;
            while (fnode != null) {
                
                sum += fnode.data;
                fnode = fnode.next;
            }
        }
        if (node != null) {
            postOrder(node.left);
            postOrder(node.right);
            txt_area_pre_in_post.append(node.data + "," + sum + "\n");
            node.printFileList();
        }
    }
    private void btn_printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_printActionPerformed
        // TODO add your handling code here:
        txt_area_pre_in_post.setText("");
        if (rbtn_pre.isSelected()) {
            txt_area_pre_in_post.append("PRE ORDER:\n");
            preOrder(BilginYilmazTreeGui.BTree.root);

        }
        if (rbtn_post.isSelected()) {
            txt_area_pre_in_post.append("POST ORDER:\n");
            postOrder(BilginYilmazTreeGui.BTree.root);

        }
        if (rbtn_ino.isSelected()) {
            txt_area_pre_in_post.append("IN ORDER:\n");
            inOrder(BilginYilmazTreeGui.BTree.root);

        }
    }//GEN-LAST:event_btn_printActionPerformed

    private void btn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchActionPerformed
        // TODO add your handling code here:
        try{
        String word=txt_search.getText().toLowerCase();
        
        BilginYilmazBNode newn=BilginYilmazTreeGui.BTree.root;
         while (true) {
             
                if(newn.data.compareTo(word)==0){
                    
                    txt_search_area.setText(word);
                    BilginYilmazFNode fnode=newn.file_list.head;
                    int sum=0;
                while(fnode!=null){
                sum+=fnode.data;
                txt_search_area.append(" "+fnode.file_name+",");
                fnode=fnode.next;
                }
                txt_search_area.append(sum+"\n");
                break;
                }
                else if(newn.data.compareTo(word)<0){
                newn=newn.right;
                }
                else if(newn.data.compareTo(word)>0){
                newn=newn.left;
                }
            }}
        catch(Exception E){
            JOptionPane.showMessageDialog(null, "Word doesn't exist!");
        }
        
    }//GEN-LAST:event_btn_searchActionPerformed

